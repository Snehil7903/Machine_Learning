# And ROC-AUC?

# The Area Under the ROC Curve (AUC) summarizes that entire graph into a single number.

# Think of it as:

# AUC = 1.0 → Excellent separation between classes.
# AUC = 0.5 → About as good as random guessing.

# An AUC closer to 1 means the model generally ranks positive examples above negative ones, regardless of the exact threshold.